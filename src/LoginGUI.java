    private String imgUrl;
    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (radServer.isSelected()) {
            this.setVisible(false);
            Thread th = new Thread() {
                public void run() {
                    User l = new Server_User(txtName.getText(), new ImageIcon(imgUrl));
                    ChatGUI chat = new SingleChat(l);
                    chat.display();
                }
            };
            th.start();

        } else {
            Configuration.IpServer = txtIpServer.getText();
            this.setVisible(false);
            Thread th = new Thread() {
                public void run() {
                    User l = new Client_User(txtName.getText(), new ImageIcon(imgUrl));
                    ChatGUI chat = new SingleChat(l);
                    chat.display();
                }
            };
            th.start();

        }


    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnAvatarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAvatarActionPerformed
        // Create a file chooser to open the dialog to choose a file.
        JFileChooser jFileChooser = new JFileChooser();
        // Set the title of the dialog.
        jFileChooser.setDialogTitle("Choose a image to send.");
        int vt1 = ClassLoader.getSystemResource("avatarFolder").toString().indexOf("/");
        int vt2 = ClassLoader.getSystemResource("avatarFolder").toString().indexOf("ChatApp_Demo");
        jFileChooser.setCurrentDirectory(new File(ClassLoader.getSystemResource("avatarFolder").toString().substring(vt1 + 1, vt2 + 12) + "/src/avatarFolder"));

        // Show the dialog and if a file is chosen from the file chooser execute the following statements.
        if (jFileChooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            String dir = jFileChooser.getCurrentDirectory().toString();
            String fileName = jFileChooser.getSelectedFile().getName();
            imgUrl = dir + "\\" + fileName;
            ImageIcon img = new ImageIcon(imgUrl); //Thu muc icon
            Image i12 = img.getImage().getScaledInstance(140, 140, Image.SCALE_DEFAULT);
            ImageIcon i13 = new ImageIcon(i12);
            lblAvatar.setIcon(i13);
            lblAvatar.setText("");
        }
    }//GEN-LAST:event_btnAvatarActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            ImageIcon img = new ImageIcon(ClassLoader.getSystemResource("chatting/application/icons/logo2.png")); //Thu muc icon
            Image i12 = img.getImage().getScaledInstance(140, 140, Image.SCALE_DEFAULT);
            ImageIcon i13 = new ImageIcon(i12);
            lblAvatar.setIcon(i13);
            imgUrl = ClassLoader.getSystemResource("chatting/application/icons").toString().substring(6) + "/logo2.png";

            this.getRootPane().setDefaultButton(btnLogin);

            txtIpServer.setText(InetAddress.getLocalHost().getHostAddress());
            txtIpServer.setEnabled(false);
        } catch (UnknownHostException ex) {
            Logger.getLogger(LoginGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

    private void radServerStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_radServerStateChanged
        if (radServer.isSelected()) {
            try {
                txtIpServer.setText(InetAddress.getLocalHost().getHostAddress());
                txtIpServer.setEnabled(false);
            } catch (UnknownHostException ex) {
                Logger.getLogger(LoginGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            txtIpServer.setEnabled(true);
        }
    }//GEN-LAST:event_radServerStateChanged

